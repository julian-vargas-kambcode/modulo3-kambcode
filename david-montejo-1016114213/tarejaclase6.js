let numeros = [1, 2, 3, 4, 5];
let resultados = [];

// Utiliza un bucle for para multiplicar cada número por 2 y almacenar el resultado en un nuevo array
for (let i = 0; i < numeros.length; i++) {
    resultados.push(numeros[i] * 2);
}

// Imprime el nuevo array
console.log("El nuevo array con los números multiplicados por 2 es:", resultados);
